    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        String titulos[] = {"Nombre", "Anio", "Genero", "Calificacion"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        List<Disco> listaDiscos = control.traerDiscos(id);
        
        if(listaDiscos != null){
            for(Disco actual : listaDiscos){
                Object objeto [] = {actual.getNombre(), actual.getAnio(), actual.getGenero().getNombre(), actual.getCalificacion()};
                modeloTabla.addRow(objeto);
            }
        }
        
        tablaDiscografia.setModel(modeloTabla);
    }
